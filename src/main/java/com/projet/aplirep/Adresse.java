package com.projet.aplirep;

public class Adresse {

    int numero;
    String nomRue;
    String nomVille;
    String codePostal;
    String nomPays;

    public Adresse(int numero, String nomRue, String nomVille, String codePostal, String nomPays) {
        this.numero = numero;
        this.nomRue = nomRue;
        this.nomVille = nomVille;
        this.codePostal = codePostal;
        this.nomPays = nomPays;
    }
}
